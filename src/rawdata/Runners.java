package rawdata;

public class Runners {
	String status;
	int sortPriority;
	double bsp;
	int id;
	String name;
	String hc;
	
	//Constructor
	public Runners() {
	}
	public String getStatus() {return status;}
	public void setStatus(String status) {this.status = status;}
	public int getSortPriority() {return sortPriority;}
	public void setSortPriority(int sortPriority) {this.sortPriority = sortPriority;}
	public double getBsp() {return bsp;}
	public void setBsp(double bsp) {this.bsp = bsp;}
	public int getId() {return id;}
	public void setId(int id) {this.id = id;}
	public String getName() {return name;}
	public void setName(String name) {this.name = name;}
	public String getHc() {return hc;}
	public void setHc(String hc) {this.hc = hc;}
} // class Runners
